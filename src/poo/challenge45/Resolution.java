package poo.challenge45;

public class Resolution {
    private int width;
    private int hieght;

    public Resolution(int width, int hieght) {
        this.width = width;
        this.hieght = hieght;
    }

    public int getWidth() {
        return width;
    }

    public int getHieght() {
        return hieght;
    }
}
